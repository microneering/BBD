/*
 * This package implements a thin layer on top of JDBC that 
 * encapsulates a small subset of JDBC that may be used to handle most
 * common application integration with database.
 *<BR>
 * This package is designed to remove references to database schema from
 * the application layer.  This includes table name, table column names,
 * table relationships, constraints, and grants.
 *<BR>
 * This design approach may be used with any database supporting 
 * stored procedures and any database interface like JDBC.
 *<BR>
 * The Black Box Database (BBD) classes are distributed in the bbd.jar 
 * file that results from building this package.
 *
 * 
 *
 */

